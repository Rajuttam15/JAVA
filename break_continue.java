import java.util.*;

public class break_continue {
    public static void main(String args[]){
        Scanner sc=new Scanner(System.in);
        int num=sc.nextInt();

        for(int i=0;i<num;i++){
            if(i==8){
                
                
                break;
            }
            System.out.print(i);
        }
        
    }
}
